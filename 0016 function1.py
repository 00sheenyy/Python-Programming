# def to declare a function/procedure. Functions return a value using the keyword return
def square(number):
    return number * number

result = square(5)
print("The square of 5 is:", result)

# a Procedure doesn't return any values so no return keyword is used.
def greet(name):
    print("Hello, " + name + "!")

greet("yourname")

